def process_api_interfaces(
    api_gav: Gav,
    proj_gav: Gav,
    api_interface_list: list,
    *,
    author: str = AUTHOR_DEFAULT
) -> str:
    print(f'api_interface_list: {api_interface_list}')
    # get the maven dependencies for the module containing the jaxrs Interface from which the service will be generated.
    service_implementation_classpath = spf.run_subprocess_capture_output([str(MAVEN_CP_PY), str(api_gav)]).strip()
    print (f'service_implementation_classpath: {service_implementation_classpath}')

    # the java package for the project to be generated.
    generated_package = jmf.get_package(proj_gav)

    # the directory of the java code.
    model_path = jmf.get_model_path(proj_gav)
    test_model_path = jmf.get_test_model_path(proj_gav)

    for api_interface_string in api_interface_list:
        process_api_interface(
            api_interface_string,
            generated_package, api_gav,
            model_path,
            test_model_path,
            service_implementation_classpath,
            author=author
        )

    remove_template_placeholder_files(model_path, test_model_path)

    return service_implementation_classpath


def process_api_interface(
    service_interface_name: str,
    generated_package: str,
    service_interface_gav: Gav,
    model_path: Path,
    test_model_path: Path,
    service_implementation_classpath: str,
    *,
    author: str = AUTHOR_DEFAULT
) -> None:
    """
    Take the impl code generated from the interface.
    Process to create method code for Impl and Service Classes.
    Write that text to the respective files on the file system.
    """
    # Run the tool to get the generated code from the jaxrs Interface.
    # This code is shared between both the service Impl class and the service class.
    generated_impl = generate_impl(
        service_interface_name,
        generated_package,
        service_implementation_classpath
    )
    print(f'generated_impl: {generated_impl}')

    method_name_to_method_dict = get_method_name_to_method_dict(generated_impl)
    method_to_args_dict = get_method_to_args_dict(
        method_name_to_method_dict.keys(),
        generated_impl
    )

    service_interface_name_without_package = service_interface_name.split('.')[-1]

    jaxrs_service_package = jmf.get_package(service_interface_gav)
    # the generated content for the service Impl class,
    # with the methods calling the service from the Impl class.
    impl_class_str = impl_template_replacment(
        generated_package,
        service_interface_name_without_package,
        jaxrs_service_package,
        author=author
    )
    service_interface_impl = generate_service_interface_impl(
        method_name_to_method_dict,
        method_to_args_dict,
        impl_class_str
    )

    # the generated content for the service class
    service_class_str = service_template_replacement(
        generated_package,
        service_interface_name_without_package,
        author=author
    )
    service_class_delegate = generate_service_class_delegate(
        method_name_to_method_dict.values(),
        service_class_str
    )

    # the generated content for the service Impl test class
    impl_class_test_str = impl_template_test_replacement(
        generated_package,
        service_interface_name_without_package,
        author=author
    )
    service_interface_impl_test = generate_service_interface_impl_test(
        impl_class_test_str
    )

    # the generated content for the service test class
    service_class_test_str = service_template_test_replacement(
        generated_package,
        service_interface_name_without_package,
        author=author
    )
    service_class_delegate_test = generate_service_class_delegate_test(
        service_class_test_str
    )

    # the template java impl class file.
    impl_template_file_path = model_path / 'impl'
    impl_template_file_name = service_interface_name_without_package + 'Impl.java'

    # the template java bean class file.
    service_template_file_path = model_path
    service_template_file_name = service_interface_name_without_package + 'Service.java'

    # the template java impl test class file.
    impl_template_test_file_path = test_model_path / 'impl'
    impl_template_test_file_name = service_interface_name_without_package + 'ImplTest.java'

    # the template java bean test class file.
    service_template_test_file_path = test_model_path
    service_template_test_file_name = service_interface_name_without_package + 'ServiceTest.java'

    # Write files to their respective locations on the file system.
    filef.make_file(
        impl_template_file_path / Path(impl_template_file_name), 
        file_content=service_interface_impl
    )

    filef.make_file(
        service_template_file_path / Path(service_template_file_name), 
        file_content=service_class_delegate
    )

    filef.make_file(
        impl_template_test_file_path / Path(impl_template_test_file_name),
        file_content=service_interface_impl_test
    )

    filef.make_file(
        service_template_test_file_path / Path(service_template_test_file_name),
        file_content=service_class_delegate_test
    )


# Take the class content stub for the Impl class and add the processed method code to it,
# replacing the 'return null;' in the method stubs with 'return service.method(args)'.
def generate_service_interface_impl(
    method_name_to_method_dict: dict,
    method_to_args_dict: dict,
    impl_class_str: str
) -> str:
    modified_methods_list = []
    # replace methods' 'return null;' with 'return service.method(args)'.
    for method_name, method_string in method_name_to_method_dict.items():
        call_string = f'service.{method_name}({method_to_args_dict[method_name]})'
        modified_methods_list.append(method_string.replace('null', call_string))

    impl_methods = '\n\n'.join(modified_methods_list) + '\n\n'
    # print (f'impl_methods: {impl_methods}'

    # write methods into Impl class and return
    #TODO check re.sub(r'\}\s*$',
    return re.sub('}', impl_methods + '}', impl_class_str)


# Take the class content stub for the Service class and add the processed method code to it.
def generate_service_class_delegate(method_list: list, service_class_str: str) -> str:
    methods = '\n\n'.join(method_list) + '\n\n'
    # print ('methods: ' + str(methods))

    # strip @Overrides from generated code methods
    # write stripped methods into service class string and return
    return re.sub('}', methods.replace('@Override\n', '') + '}', service_class_str)


