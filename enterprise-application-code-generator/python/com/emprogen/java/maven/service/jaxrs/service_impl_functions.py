def remove_template_placeholder_files(model_path: Path, test_model_path: Path) -> None:
    filef.delete_file(model_path / Path('TemplateService.java'))
    filef.delete_file(model_path / Path('impl') / Path('TemplateImpl.java'))

    filef.delete_file(test_model_path / Path('TemplateServiceTest.java'))
    filef.delete_file(test_model_path / Path('impl') / Path('TemplateImplTest.java'))


def is_class_file_an_api_definition(class_file_content: str) -> bool:
    has_api_def_ann = False
    is_interface = False
    match = re.search(
        r'org.eclipse.microprofile.openapi.annotations.OpenAPIDefinition',
        class_file_content
    )
    if match:
        has_api_def_ann = True
    if 'public interface' in class_file_content:
        is_interface = True
    return has_api_def_ann and is_interface


def generate_impl(
    service_interface_name: str,
    generated_package: str,
    service_implementation_classpath: str) -> str:
    # run the tool to get the generated code from the jaxrs Interface.
    classpath = f"{service_implementation_classpath}:{GENERATE_IMPL_CLASSPATH}"
    cmd = [
        'java',
        '-cp',
        classpath,
        GEN_IMPL_CLASS,
        service_interface_name,
        generated_package
    ]
    return spf.run_subprocess_capture_output(cmd)


def get_method_name_to_method_dict(generated_impl: str) -> dict:
    # get methods code from generated code
    pattern = re.compile(r'(\s{4}@Override(\n|.)+?})')
    matches = re.finditer(pattern, generated_impl)
    match_list = [match.group() for match in matches]
    # print (f'match_list: {match_list}')

    # get method names from generated code
    # methods will be \s methodName[\s zero or more whitespace, then left parentheses. (]
    method_name_pattern = re.compile(r'(?<=\s)([a-zA-Z0-9$_]+)(?=\s*\()')
    method_name_matches = re.finditer(method_name_pattern, generated_impl)
    method_name_match_list = [match.group() for match in method_name_matches]
    # print (f'method_name_match_list: {method_name_match_list}')

    method_name_to_method_dict = dict(zip(method_name_match_list, match_list))
    return method_name_to_method_dict


def get_method_to_args_dict(
    method_name_match_list: list,
    generated_impl: str
) -> dict:
    # get the arg list for each method
    arg_match_list = []

    for method_name in method_name_match_list:
        # get the arg list for the method
        arg_pattern = re.compile(r'(?<=\s)' + method_name + '\s*\((.*?)\)')
        arg_match = re.search(arg_pattern, generated_impl)
        if None != arg_match:
            arg_match_list.append(arg_match.group(1))
        # print(f'arg_match_list: {arg_match_list}')

    # dict of methodName:rawArgList
    method_to_args_dict = dict(zip(method_name_match_list, arg_match_list))
    print (f'method_to_args_dict: {method_to_args_dict}')

    # parse the args to remove types.
    for method_name, arg_list in method_to_args_dict.items():
        print (f'method_name: {method_name}')
        print (f'arg_list: {arg_list}')
        # get the arg type:value pairs
        arg_type_var_list = arg_list.split(',')
        # print (f'arg_type_var_list: {arg_type_var_list}')
        # get the arg var names
        arg_vars = []
        for arg_type_var in arg_type_var_list:
            # print (f'arg_type_var: {arg_type_var}')
            arg_array_tmp = arg_type_var.strip().split(' ')
            if len(arg_array_tmp) > 1:
                arg_vars.append(arg_array_tmp[1])
        # print (f'arg_vars: {arg_vars}')

        method_to_args_dict[method_name] = ', '.join(arg_vars)

    print (f'method_to_args_dict2: {method_to_args_dict}')
    return method_to_args_dict


