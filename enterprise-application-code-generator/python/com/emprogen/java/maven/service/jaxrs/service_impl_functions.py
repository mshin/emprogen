def impl_template_replacment(
    package: str,
    jaxrs_service_interface: str,
    jaxrs_service_package: str,
    *,
    author: str = AUTHOR_DEFAULT
) -> str:
    """
    Returns the text of the impl template file, with the text substituted with params.
    jaxrs_service_interface is name only, without package.
    """
    return (
        IMPL_TEMPLATE_TEXT
        .replace('${package}', package)
        .replace('${jaxrs_service_interface}', jaxrs_service_interface)
        .replace('${jaxrs_service_package}', jaxrs_service_package)
        .replace('${author}', author)
    )


